/**
 * Available window types.
 */
public enum WindowType {

    /**
     * Time-based window.
     */
    TIME_WINDOW,

    /**
     * Count-based window.
     */
    COUNT_WINDOW,

    /**
     * Partitioned count-based window.
     */
    PARTITIONED_COUNT_WINDOW

}

